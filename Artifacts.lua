function ArchDB_ArtifactList_Setup(raceIndex, raceName)
	if raceName == "Dwarf" then
		ADB_Dwarf(raceIndex);
	elseif raceName == "Draenei" then
		ADB_Draenei(raceIndex);
	elseif raceName == "Fossil" then
		ADB_Fossil(raceIndex);
	elseif raceName == "Night Elf" then
		ADB_NightElf(raceIndex);
	elseif raceName == "Nerubian" then
		ADB_Nerubian(raceIndex);
	elseif raceName == "Orc" then
		ADB_Orc(raceIndex);
	elseif raceName == "Tol'vir" then
		ADB_Tolvir(raceIndex);
	elseif raceName == "Troll" then
		ADB_Troll(raceIndex);
	elseif raceName == "Vrykul" then
		ADB_Vrykul(raceIndex);
	elseif raceName == "Pandaren" then
		ADB_Pandaren(raceIndex);
	elseif raceName == "Mogu" then
		ADB_Mogu(raceIndex);
	elseif raceName == "Mantid" then
		ADB_Mantid(raceIndex);
	elseif raceIndex == 1 then
		ADB_Dwarf(raceIndex);
	elseif raceIndex == 2 then
		ADB_Draenei(raceIndex);
	elseif raceIndex == 3 then
		ADB_Fossil(raceIndex);
	elseif raceIndex == 4 then
		ADB_NightElf(raceIndex);
	elseif raceIndex == 5 then
		ADB_Nerubian(raceIndex);
	elseif raceIndex == 6 then
		ADB_Orc(raceIndex);
	elseif raceIndex == 7 then
		ADB_Tolvir(raceIndex);
	elseif raceIndex == 8 then
		ADB_Troll(raceIndex);
	elseif raceIndex == 9 then
		ADB_Vrykul(raceIndex);
	elseif raceIndex == 10 then
		ADB_Mantid(raceIndex);
	elseif raceIndex == 11 then
		ADB_Pandaren(raceIndex);
	elseif raceIndex == 12 then
		ADB_Mogu(raceIndex);
	else
--		DEFAULT_CHAT_FRAME:AddMessage("ArchDB: Unknown Race Setup for "..raceName.." "..raceIndex);
		return false;
	end
	return true;
end

